lcdPlotT:
        push {r1, r3}
        ldr r3, =(lcdWidth*2)
        mul r1, r3
        add r1, r0
        add r1, r0
        ldr r3, =lcdVideoBuffer
        strh r2, [r3, r1]
        pop {r1, r3}
