lcdWidth        = 320
lcdHeight       = 240
        
@ lcd_puts r0=x, r1=y, r2->string, r3=color
@
lcdPutsT:
        push {r4, r5, r6, r7, lr}

        mov r4, r0
        mov r5, r1
        mov r6, r2
lcdLoop1:
        mov r0, r4
        mov r1, r5
        ldrb r2, [r6, #0]       @ get the char

        mov r7, #1
        mov lr, r7
        ldr r7, =(lcdPutchT+1)
        add lr, pc
        bx r7

        add r6, r6, #1
        mov r1, #0
        ldrb r1, [r6, #0]       @ check for 0
        cmp r1, #0
         beq lcdDone1
        
        add r4, r4, #6
        ldr r1, =lcdWidth
        sub r1, #6
        cmp r4, r1
         blt lcdLoop1
        mov r4, #4

        add r5, r5, #6
        ldr r1, =lcdHeight
        sub r1, #6
        cmp r5, r1
         bge lcdDone1
        b lcdLoop1
lcdDone1:
        pop {r4, r5, r6, r7}
        pop {r0}
        bx r0

@ lcdPutchT r0=x, r1=y, r2=character, r3=color
@

lcdPutchT:
        push {r2, r3, r4, r5, r6, lr}  
        mov r5, r2
        ldr r4, =font1_20       @ assume space
        cmp r5, #0x20           @ ' '?
         blt lcdNota
        cmp r5, #0x5a           @ 'Z'
         bgt lcdNota
        ldr r4, =font1tab
        sub r5, r5, #0x20
        lsl r5, r5, #2
        add r5, r4, r5
        ldr r4, [r5, #0]        @ get address of sprite for it...
lcdNota:    
        mov r2, #6
        mov r5, r3
        mov r3, #6
        mov r6, #1
        mov lr, r6
        ldr r6, =(lcdSpritecT+1)
        add lr, pc
        bx r6
        pop {r2, r3, r4, r5, r6}
        pop {r0}
        bx r0


@ lcdSpritecT r0=x, r1=y, r2=width, r3=height, r4->data, r5=colorF
@
    
lcdSpritecT:
        push {r0, r1, r2, r3, r4, r5, r6, r7}

        mov r7, r5
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1          @ r5-> start of vid mem for it

lcdLoop2:
        mov r0, r2
        mov r6, #0
lcdLoop3:
        ldrh r1, [r4, #0]
        cmp r1, #0
         beq lcdNco
        mov r1, r7          @ non zero! Set to required color.
lcdNco:
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop3

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop2

        pop {r0, r1, r2, r3, r4, r5, r6, r7}
        bx lr

@ lcdRectfT r0=x, r1=y, r2=width, r3=height, r4=color
@   
lcdRectfT:
        push {r1, r2, r3, r4, r5, r6}
        mov r5, #0xff
        and r4, r5
        mov r5, r4
        lsl r4, #8
        add r4, r5

        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1              @ r5-> start of vid mem for it
lcdLoop4:
        mov r0, r2
        mov r6, #0
lcdLoop5:
        strh r4, [r5, r6]
        add r6, #2
        sub r0, #1
         bne lcdLoop5
        ldr r0, =(lcdWidth*2)
        add r5, r0              @ move on to next line...
        sub r3, #1
         bne lcdLoop4
        pop {r1, r2, r3, r4, r5, r6}
        bx lr

@ lcdPlotT  r0=x, r1=y, r2=c
@
    
lcdPlotT:
        push {r1, r3}
        ldr r3, =(lcdWidth*2)
        mul r1, r3
        add r1, r0
        add r1, r0
        ldr r3, =lcdVideoBuffer
        strh r2, [r3, r1]
        pop {r1, r3}
        bx lr

@ lcdGetPixelT r0=x, r1=y
@              r0 = pixel

lcdGetPixelT:
        push {r1, r3}
        ldr r3, =(lcdWidth*2)
        mul r1, r3
        add r1, r0
        add r1, r0
        ldr r3, =lcdVideoBuffer
        ldrh r0, [r3, r1]
        pop {r1, r3}
        bx lr

@ lcdSpriteT r0=x, r1=y, r2=width, r3=height, r4->data
@
    
lcdSpriteT:
        push {r0, r1, r2, r3, r4, r5, r6}
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1
lcdLoop6:
        mov r0, r2
        mov r6, #0
lcdLoop7:
        ldrh r1, [r4, #0]
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop7

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop6
        pop {r0, r1, r2, r3, r4, r5, r6}
        bx lr

