        ldr r4, [sp, #12]
        callThumb r5, lcdRectfT
        pop {r4, r5}
        pop {r0}
        bx r0

@ lcdScrollUpT r0=lines, r1=color
@
.globl lcdScrollUpT
.thumb_func
lcdScrollUpT:
        push {r0, r1, r2, r3, r4}
        cmp r0, #0
        beq noneed
        ldr r2, =(lcdWidth*2)
        mul r2, r0
        ldr r3, =lcdVideoBuffer
        ldr r4, =(lcdWidth*lcdHeight*2)
        sub r4, r2

        @ r2 = count for last bit clear (+src offset)
        @ r4 = count for main scroll
        @ r3 -> dest
lcdLoop0:
        ldrh r0, [r3, r2]
        strh r0, [r3]               @ copy pixel accross
        add r3, #2
        sub r4, #2
        bne lcdLoop0
lcdLoop0a:
        strh r1, [r3]
        add r3, #2
        sub r2, #2
        bne lcdLoop0a
noneed:
        pop {r0, r1, r2, r3, r4}
        bx lr
        
        
@ lcdPutsT r0=x, r1=y, r2->string, r3=color
@
.globl lcdPutsT
.thumb_func
lcdPutsT:
        push {r4, r5, r6, r7, lr}

        mov r4, r0
        mov r5, r1
        mov r6, r2
lcdLoop1:
        mov r0, r4
        mov r1, r5
        ldrb r2, [r6, #0]       @ get the char

        callThumb r7, lcdPutchT

        add r6, r6, #1
        mov r1, #0
        ldrb r1, [r6, #0]       @ check for 0
        cmp r1, #0
         beq lcdDone1
        
        add r4, r4, #6
        ldr r1, =lcdWidth
        sub r1, #6
        cmp r4, r1
         blt lcdLoop1
        mov r4, #4

        add r5, r5, #6
        ldr r1, =lcdHeight
        sub r1, #6
        cmp r5, r1
         bge lcdDone1
        b lcdLoop1
lcdDone1:
        pop {r4, r5, r6, r7}
        pop {r0}
        bx r0

@ lcdPutchT r0=x, r1=y, r2=character, r3=color
@
.globl lcdPutchT
.thumb_func
lcdPutchT:
        push {r2, r3, r4, r5, r6, lr}  
        ldr r5, =0xff
        and r5, r2
        ldr r4, =font1_20       @ assume space
        cmp r5, #0x20           @ ' '?
         blt lcdNota
        cmp r5, #0x7e
         bgt lcdNota
        ldr r4, =font1tab
        sub r5, #0x20
        lsl r5, #2
        add r5, r4
        ldr r4, [r5, #0]        @ get address of sprite for it...
lcdNota:
        mov r2, #6
        mov r5, r3
        mov r3, #6

        callThumb r6, lcdSpritecT
        pop {r2, r3, r4, r5, r6}
        pop {r0}
        bx r0


@ lcdSpritecT r0=x, r1=y, r2=width, r3=height, r4->data, r5=colorF
@   
.globl lcdSpritecT
.thumb_func
lcdSpritecT:
        push {r0, r1, r2, r3, r4, r5, r6, r7}

        mov r7, r5
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1          @ r5-> start of vid mem for it

lcdLoop2:
        mov r0, r2
        mov r6, #0
lcdLoop3:
        ldrh r1, [r4, #0]
        cmp r1, #0
         beq lcdNco
        mov r1, r7          @ non zero! Set to required color.
        b lcdDco
lcdNco: ldr r1, fontBG
lcdDco:
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop3

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop2

        pop {r0, r1, r2, r3, r4, r5, r6, r7}
        bx lr

@ lcdRectfT r0=x, r1=y, r2=width, r3=height, r4=color
@   
.globl lcdRectfT
.thumb_func
lcdRectfT:
        push {r1, r2, r3, r4, r5, r6}

        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1              @ r5-> start of vid mem for it
lcdLoop4:
        mov r0, r2
        mov r6, #0
lcdLoop5:
        strh r4, [r5, r6]
        add r6, #2
        sub r0, #1
         bne lcdLoop5
        ldr r0, =(lcdWidth*2)
        add r5, r0              @ move on to next line...
        sub r3, #1
         bne lcdLoop4
        pop {r1, r2, r3, r4, r5, r6}
        bx lr

@ lcdPlotT  r0=x, r1=y, r2=c
