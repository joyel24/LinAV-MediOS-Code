lcdVideoBuffer  = 0x3fb0000
lcdVideoBuffer2 = 0x3f60000
lcdWidth        = 320
lcdHeight       = 240


@ lcdScrollUpT r0=lines, r1=color
@
lcdScrollUpT:
        push {r0, r1, r2, r3, r4}
        cmp r0, #0
        beq noneed
        ldr r2, =(lcdWidth*2)
        mul r2, r0
        ldr r3, =lcdVideoBuffer
        ldr r4, =(lcdWidth*lcdHeight*2)
        sub r4, r2

        @ r2 = count for last bit clear (+src offset)
        @ r4 = count for main scroll
        @ r3 -> dest
lcdLoop0:
        ldrh r0, [r3, r2]
        strh r0, [r3]               @ copy pixel accross
        add r3, #2
        sub r4, #2
        bne lcdLoop0
lcdLoop0a:
        strh r1, [r3]
        add r3, #2
        sub r2, #2
        bne lcdLoop0a
noneed:
        pop {r0, r1, r2, r3, r4}
        bx lr
        
        
@ lcdPutsT r0=x, r1=y, r2->string, r3=color
@
lcdPutsT:
        push {r4, r5, r6, r7, lr}

        mov r4, r0
        mov r5, r1
        mov r6, r2
lcdLoop1:
        mov r0, r4
        mov r1, r5
        ldrb r2, [r6, #0]       @ get the char

        callThumb r7, lcdPutchT

        add r6, r6, #1
        mov r1, #0
        ldrb r1, [r6, #0]       @ check for 0
        cmp r1, #0
         beq lcdDone1
        
        add r4, r4, #6
        ldr r1, =lcdWidth
        sub r1, #6
        cmp r4, r1
         blt lcdLoop1
        mov r4, #4

        add r5, r5, #6
        ldr r1, =lcdHeight
        sub r1, #6
        cmp r5, r1
         bge lcdDone1
        b lcdLoop1
lcdDone1:
        pop {r4, r5, r6, r7}
        pop {r0}
        bx r0

@ lcdPutchT r0=x, r1=y, r2=character, r3=color
@
lcdPutchT:
        push {r2, r3, r4, r5, r6, lr}  
        ldr r5, =0xff
        and r5, r2
        ldr r4, =font1_20       @ assume space
        cmp r5, #0x61           @ 'a'
         blt lcdNotb
        cmp r5, #0x7a           @ 'z'
         bgt lcdNotb
        sub r5, #0x20           @ *** convert to upper case for now ***
lcdNotb:
        cmp r5, #0x20           @ ' '?
         blt lcdNota
        cmp r5, #0x7e
         bgt lcdNota
        ldr r4, =font1tab
        sub r5, #0x20
        lsl r5, #2
        add r5, r4
        ldr r4, [r5, #0]        @ get address of sprite for it...
lcdNota:
        mov r2, #6
        mov r5, r3
        mov r3, #6

        callThumb r6, lcdSpritecT
        pop {r2, r3, r4, r5, r6}
        pop {r0}
        bx r0


@ lcdSpritecT r0=x, r1=y, r2=width, r3=height, r4->data, r5=colorF
@   
lcdSpritecT:
        push {r0, r1, r2, r3, r4, r5, r6, r7}

        mov r7, r5
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1          @ r5-> start of vid mem for it

lcdLoop2:
        mov r0, r2
        mov r6, #0
lcdLoop3:
        ldrh r1, [r4, #0]
        cmp r1, #0
         beq lcdNco
        mov r1, r7          @ non zero! Set to required color.
lcdNco:
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop3

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop2

        pop {r0, r1, r2, r3, r4, r5, r6, r7}
        bx lr

@ lcdRectfT r0=x, r1=y, r2=width, r3=height, r4=color
@   
lcdRectfT:
        push {r1, r2, r3, r4, r5, r6}

        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1              @ r5-> start of vid mem for it
lcdLoop4:
        mov r0, r2
        mov r6, #0
lcdLoop5:
        strh r4, [r5, r6]
        add r6, #2
        sub r0, #1
         bne lcdLoop5
        ldr r0, =(lcdWidth*2)
        add r5, r0              @ move on to next line...
        sub r3, #1
         bne lcdLoop4
        pop {r1, r2, r3, r4, r5, r6}
        bx lr

@ lcdPlotT  r0=x, r1=y, r2=c
@    
lcdPlotT:
        push {r1, r3}
        ldr r3, =(lcdWidth*2)
        mul r1, r3
        add r1, r0
        add r1, r0
        ldr r3, =lcdVideoBuffer
        strh r2, [r3, r1]
        pop {r1, r3}
        bx lr

@ lcdGetPixelT r0=x, r1=y
@              r0 = pixel
lcdGetPixelT:
        push {r1, r3}
        ldr r3, =(lcdWidth*2)
        mul r1, r3
        add r1, r0
        add r1, r0
        ldr r3, =lcdVideoBuffer
        ldrh r0, [r3, r1]
        pop {r1, r3}
        bx lr

@ lcdSpriteT r0=x, r1=y, r2=width, r3=height, r4->data
@
lcdSpriteT:
        push {r0, r1, r2, r3, r4, r5, r6}
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1
lcdLoop6:
        mov r0, r2
        mov r6, #0
lcdLoop7:
        ldrh r1, [r4, #0]
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop7

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop6
        pop {r0, r1, r2, r3, r4, r5, r6}
        bx lr

@ lcdInitT
@
lcdInitT:
        push {lr}
        mov r2, #0
        ldr r0, =0x30800    
        ldr r1, =0x4071
        strh r1, [r0]
        ldr r1, =0x0f40
        strh r1, [r0, #0x2]
        ldr r1, =0x8100
        strh r1, [r0, #0x4]
        ldr r1, =0x4c0f
        strh r1, [r0, #0x6]
        ldr r1, =0xaaaa
        strh r1, [r0, #0x8]
        strh r2, [r0, #0xa]
        strh r2, [r0, #0xc]
        strh r2, [r0, #0xe]
        strh r2, [r0, #0x10]
        strh r2, [r0, #0x12]
        strh r2, [r0, #0x14]
        strh r2, [r0, #0x16]
        strh r2, [r0, #0x18]
        ldr r1, =0x02e0
        strh r1, [r0, #0x1a]
        ldr r1, =0x0085             @ X offset for display
        strh r1, [r0, #0x1c]
        ldr r1, =0x0280             @ WIDTH in pixels (Number processed)
        strh r1, [r0, #0x1e]
        ldr r1, =0x0103
        strh r1, [r0, #0x20]
        ldr r1, =0x0013             @ Y offset for display
        strh r1, [r0, #0x22]
        ldr r1, =0x00f0             @ HEIGHT in pixels (number processed)
        strh r1, [r0, #0x24]
        strh r2, [r0, #0x26]
        strh r2, [r0, #0x28]
        strh r2, [r0, #0x2a]
        ldr r1, =0x8001
        strh r1, [r0, #0x2c]
        ldr r1, =0x0002
        strh r1, [r0, #0x2e]
        ldr r1, =0xff00             @ Brightness adder (lo)
        strh r1, [r0, #0x30]
        strh r2, [r0, #0x32]
        strh r2, [r0, #0x34]
        strh r2, [r0, #0x36]
        ldr r1, =0x0022
        strh r1, [r0, #0x38]

        @ OSD Setup...        
        callThumb1 r3, osdSetScreenConfigT, 0x2000
        callThumb2 r3, osdSetScreenPositionT, 0x79, 0x00
        callThumb2 r3, osdSetBackConfigT, 0x14, 0x02
        callThumb2 r3, osdSetMainConfigT, 0x14, 0xf9    @ d9      
        callThumb2 r3, osdSetBack2ConfigT, 0x14, 0x00
        callThumb2 r3, osdSetMain2ConfigT, 0x14, 0x00
        callThumb2 r3, osdSetBackPositionT, 0x14, 0x12
        callThumb2 r3, osdSetMainPositionT, 0x14, 0x12
        callThumb2 r3, osdSetBackSizeT, 0x280, 0xf0
        callThumb2 r3, osdSetMainSizeT, 0x280, 0xf0
        callThumb1 r1, osdSetMainOffsetT, lcdVideoBuffer
        callThumb1 r1, osdSetBackOffsetT, lcdVideoBuffer2
    
        ldr r0, =lcdVideoBuffer
        ldr r1, =(lcdWidth*lcdHeight*2)
        ldr r2, =0x0000
lcdCLS:
        strh r2, [r0, #0]
        add r0, #2
        sub r1, #1
         bne lcdCLS

        ldr r0, =lcdVideoBuffer2
        ldr r1, =(lcdWidth*lcdHeight*2)
        ldr r2, =0x0000
lcdCLS2:
        strh r2, [r0, #0]
        add r2, #1
        add r0, #2
        sub r1, #1
         bne lcdCLS2

        pop {r0}
        bx r0

        .ltorg
    
        .arm
        
