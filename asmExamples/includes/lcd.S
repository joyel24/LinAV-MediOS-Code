        bx lr

@ lcdSpriteT r0=x, r1=y, r2=width, r3=height, r4->data
@
.globl lcdSpriteT
.thumb_func
lcdSpriteT:
        push {r0, r1, r2, r3, r4, r5, r6}
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1
lcdLoop6:
        mov r0, r2
        mov r6, #0
lcdLoop7:
        ldrh r1, [r4, #0]
        add r4, #2

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoop7

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoop6
        pop {r0, r1, r2, r3, r4, r5, r6}
        bx lr

@ lcdSpriteBT r0=x, r1=y, r2=width, r3=height, r4->data
@
.globl lcdSpriteBT
.thumb_func
lcdSpriteBT:
        push {r0, r1, r2, r3, r4, r5, r6}
        ldr r5, =(lcdWidth*2)
        mul r1, r5
        add r1, r0
        add r1, r0
        ldr r5, =lcdVideoBuffer
        add r5, r1
        mov r1, #0
lcdLoopB6:
        mov r0, r2
        mov r6, #0
lcdLoopB7:
        ldrb r1, [r4, #0]
        add r4, #1

        strh r1, [r5, r6]
        add r6, #2

        sub r0, #1
         bne lcdLoopB7

        ldr r0, =(lcdWidth*2)
        add r5, r0          @ move on to next line...
        sub r3, #1
         bne lcdLoopB6
        pop {r0, r1, r2, r3, r4, r5, r6}
        bx lr
        
@ lcdInitT
@
.globl lcdInitT
.thumb_func
lcdInitT:
        push {lr}
        callThumb r0, osdInitT
        
        .ifdef hardwareAV300
        callThumb1 r3, osdSetScreenConfigT, 0x2000
        callThumb2 r3, osdSetScreenPositionT, 0x79, 0x00
        callThumb2 r3, osdSetBackConfigT, 0x14, 0x02
        callThumb2 r3, osdSetMainConfigT, 0x14, 0xf9    @ d9      
        callThumb2 r3, osdSetBack2ConfigT, 0x14, 0x00
        callThumb2 r3, osdSetMain2ConfigT, 0x14, 0x00
        callThumb2 r3, osdSetBackPositionT, 0x14, 0x12
        callThumb2 r3, osdSetMainPositionT, 0x14, 0x12
        callThumb2 r3, osdSetBackSizeT, 0x280, 0xf0
        callThumb2 r3, osdSetMainSizeT, 0x280, 0xf0
        callThumb1 r1, osdSetMainOffsetT, lcdVideoBuffer
        callThumb1 r1, osdSetBackOffsetT, lcdVideoBuffer2
        .endif

        ldr r0, =lcdVideoBuffer
        ldr r1, =(lcdWidth*lcdHeight*2)
        ldr r2, =0x0000
lcdCLS:
        strh r2, [r0, #0]
        add r0, #2
        sub r1, #1
         bne lcdCLS

        pop {r0}
        bx r0

lcdClsT:
        push {r0, r1, r2}
        ldr r0, =lcdVideoBuffer
        ldr r1, =(lcdWidth*lcdHeight*2)
        ldr r2, =0x0000
lcdCLS2:
        strh r2, [r0, #0]
        add r0, #2
        sub r1, #1
         bne lcdCLS2

        pop {r0, r1, r2}
        bx lr

