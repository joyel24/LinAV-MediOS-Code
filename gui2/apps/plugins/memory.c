int eventHandler(int evt)
{
    char buffer[40];
    int i=0,j=0;

    if(GameMode == 0)
    {
		switch (evt) {
			case BTN_OFF:
			case EVT_QUIT:
					/* get out of here */
					RELEASE(cops)
					break;

					/* move cursor left */
				case BTN_LEFT:
					setCursor(1);
                    if(x-1 >= 0)
					   x--;
					else
					   x = width-1;

					setCursor(0);
					break;

					/* move cursor right */
				case BTN_RIGHT:
					setCursor(1);
                    if(x+1 >= width)
					   x = 0;
					else
					   x++;
					setCursor(0);
					break;

					/* move cursor down */
				case BTN_DOWN:
					setCursor(1);
                    if(y+1 >= height)
					   y = 0;
					else
					   y++;
					setCursor(0);
					break;

					/* move cursor up */
				case BTN_UP:
					setCursor(1);
                    if(y-1 >= 0)
					   y--;
					else
					   y = height-1;
					setCursor(0);
					break;

					/* discover a tile (and it's neighbors if .neighbors == 0) */
				case BTN_F1:
					break;

					/* toggle flag under cursor */
				case BTN_F2:
					field[y][x].known = 1;
					displayField();
					break;

				case BTN_ON: // new game
					cops->clearScreen(COLOR_GREEN);
					init();
					displayField();
					setCursor(0);
					break;

				case BTN_F3: // settings
					cops->clearScreen(COLOR_GREEN);
					cops->putS(COLOR_BLACK, COLOR_GREEN, 295,47, "Quit");
                    GameMode = 2;
					init_settings_screen();
					break;
		}
	}
	else
	{
		switch (evt) {
		   case BTN_UP:
                if(settingParam == 0)
				   settingParam = 2;
				else
				   settingParam--;

				RefreshSettings();
			   break;

		   case BTN_DOWN:
                if(settingParam == 2)
				   settingParam = 0;
				else
				   settingParam++;

				RefreshSettings();
			   break;

		   case BTN_LEFT:

				if(settingParam == 0)
				{
 				    if(width > 10)
					{
                        width--;
                        RefreshSettings();
					}
				}
				else
				{
				    if(height > 10)
					{
                        height--;
                        RefreshSettings();
					}
				}
			    break;

		   case BTN_RIGHT:
			    if(settingParam == 0)
				{
				    if(width < (SCREEN_WIDTH-50)/PIECE_DIM)
				    {
                        width++;
                        RefreshSettings();
					}
				}
				else
				{
				    if(height < (SCREEN_HEIGHT/PIECE_DIM))
					{
                        height++;
                        RefreshSettings();
                    }
                }
			    break;

			case BTN_OFF:
			case EVT_QUIT:
                writeINI();
				cops->clearScreen(COLOR_GREEN);
                GameMode = 0;
                x=0;y=0;
				init();
				displayField();
				setCursor(0);
				break;
		}

	}
}

