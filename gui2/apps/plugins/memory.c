coord GetXY(int pos)
{
    coord xy;
    int index = 0;

    x = 0;
    y = 0;

    do
    {
        if(x+1 < width) x++; else x=0;
        if(x==0) y++;

        index++;
    }
    while ( index < pos );

    xy.x = x;
    xy.y = y;

    return xy;
}

void GenerateField()
{
    int i = 0;
    int j = 0;
    int k = 0;
    int map_size = height*width;
    int pos1 = 0;
    int pos2 = 0;
    int index = 0;
    int tmpIdent = 0;
    coord xy1;
    coord xy2;

    // fill map
    while ( index < map_size)
    {
        field[j][i].identifier = k+1;

        if(i+1 < width) i++; else i=0;
        if(i==0) j++;

        if(k+1>(map_size/2)-1) k=0; else k++;
        index++;
    }

    // unsort these pairs
    for ( i = 0; i < map_size * 20; i++ )
    {
        pos1 = rand() % map_size;
        pos2 = rand() % map_size;

        // only allow switch if not two identical cards beside each other
//        if ( !id_card_found( pos1, pairs->map[pos2] ) &&
//             !id_card_found( pos2, pairs->map[pos1] ) )
        {
            // swap
            xy1 = GetXY(pos1);
            xy2 = GetXY(pos2);

            tmpIdent = field[xy1.y][xy1.x].identifier;
            field[xy1.y][xy1.x].identifier = field[xy2.y][xy2.x].identifier;
            field[xy2.y][xy2.x].identifier = tmpIdent;
        }

    }
}


