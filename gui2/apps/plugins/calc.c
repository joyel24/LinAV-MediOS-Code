int eventHandler(int evt)
{

    switch (evt) {
        case BTN_OFF:
        case EVT_QUIT:
                /* get out of here */
                RELEASE(cops)
                break;

            /* move cursor left */
            case BTN_LEFT:
                setCursor(1);
                if(x-1 >= 0)
                    x--;
                setCursor(0);
                break;

            /* move cursor right */
            case BTN_RIGHT:
                setCursor(1);
                if(x+1 < width)
                    x++;
                setCursor(0);
                break;

            /* move cursor down */
            case BTN_DOWN:
                setCursor(1);
                if(y+1 < height)
                    y++;
                setCursor(0);
                break;

            /* move cursor up */
            case BTN_UP:
                setCursor(1);
                if(y-1 >= 0)
                    y--;
                setCursor(0);
                break;

            /* input */
            case BTN_F1:
                calculate();
                break;

            /* clear current data */
            case BTN_F3:
                erg = 0;
                tmpErg = 0;
                operation = OP_UNDEFINED;
                memset(&strCurrentNumber, 0 ,sizeof(strCurrentNumber));
                output(strCurrentNumber);
                break;
    }
}

