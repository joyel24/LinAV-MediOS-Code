int eventHandler(int evt)
{

    switch (evt) {
        case BTN_OFF:
        case EVT_QUIT:
                /* get out of here */
                RELEASE(cops)
                break;

                /* move cursor left */
            case BTN_LEFT:
                if(x-1 >= 0)
                    x--;
                break;

                /* move cursor right */
            case BTN_RIGHT:
                if(x+1 < width)
                    x++;
                break;

                /* move cursor down */
            case BTN_DOWN:
                if(y+1 < height)
                    y++;
                break;

                /* move cursor up */
            case BTN_UP:
                if(y-1 >= 0)
                    y--;
                break;

            case BTN_F2:
                break;

                /* toggle flag under cursor */
            case BTN_F1:
                break;

            case BTN_ON: // new game
                break;

            case BTN_F3: // settings
                break;
    }
}

