double mySqrt(double square)
{
    int k = 0;
    double temp = 0;
    double root= ABS(square+1)/2;

    while( ABS(root - temp) > MINIMUM ){
        temp = root;
        root = (square/temp + temp)/2;
        k++;
        if (k>10000) return 0;
    }

    return root;
}

void makeOperation()
{
    int l = 0;
    double tmp = 0;

    if(operation == OP_PLUS)
    {
        erg = tmpErg + atof(strCurrentNumber);
    }
    else if(operation == OP_MINUS)
    {
        erg = tmpErg - atof(strCurrentNumber);
    }
    else if(operation == OP_MULTI)
    {
        erg = tmpErg * atof(strCurrentNumber);
    }
    else if(operation == OP_DIV)
    {
        if(atof(strCurrentNumber) != 0)
            erg = tmpErg / atof(strCurrentNumber);
    }
    else if(operation == OP_XY)
    {
        erg = tmpErg;

        tmp = atof(strCurrentNumber);

        for(l = 0; l < tmp-1; l++)
        {
            erg *= tmpErg;
        }
    }
}

