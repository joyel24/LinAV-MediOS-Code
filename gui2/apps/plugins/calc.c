coord GetXY(int pos)
{
    coord xy;
    int index = 0;

    x = 0;
    y = 0;

    do
    {
        if(x+1 < width) x++; else x=0;
        if(x==0) y++;

        index++;
    }
    while ( index < pos );

    xy.x = x;
    xy.y = y;

    return xy;
}

