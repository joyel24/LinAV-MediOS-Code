/* discovers the tile when player clears one of them */
/* a chain reaction (of discovery) occurs if tile has no mines */
/* as neighbors */
void discover(int, int);
void discover(int x, int y){

    if(x<0) return;
    if(y<0) return;
    if(x>width-1) return;
    if(y>height-1) return;
    if(minefield[y][x].known) return;
    
    minefield[y][x].known = 1;
    if(minefield[y][x].neighbors == 0){
        discover(x-1,y-1);
        discover(x,y-1);
        discover(x+1,y-1);
        discover(x+1,y);
        discover(x+1,y+1);
        discover(x,y+1);
        discover(x-1,y+1);
        discover(x-1,y);
    }
    return;
}

static int GetTime()
{
   struct tm tm;
   int fd;
	int seconds = 0;
	char tmp[100];

   fd=open("/dev/avrtc",O_RDONLY | O_NONBLOCK);
   if (fd < 0)
	{
      printf("Can't open /dev/avrtc\n");
   }

	if(ioctl(fd,AV_RTC_GET_TIME_IOC,&tm)<0)
	{
      printf("Error getting time and date\n");
   }
   close(fd);

	sprintf(tmp, "%02d",tm.tm_hour);
   seconds = atoi(tmp)*3600;
	sprintf(tmp, "%02d",tm.tm_min);
   seconds += atoi(tmp)*60;
	sprintf(tmp, "%02d",tm.tm_sec);
   seconds += atoi(tmp);

	return seconds;
}


