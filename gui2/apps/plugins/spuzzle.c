int eventHandler(int evt)
{
    char buffer[40];
    int i=0,j=0;
    int gameover = 0;
    int oldx = x;
    int oldy = y;
    int oldIdent = 0;

    switch (evt) {
        case BTN_OFF:
        case EVT_QUIT:
                /* get out of here */
                RELEASE(cops)
                break;

                /* move cursor left */
            case BTN_LEFT:
                if(x-1 >= 0)
                {
                    x--;
                    turns++;
                    showTurns();
                    field[oldy][oldx] = field[y][x];
                    field[y][x] = 0;
                    displayField();
                    if(checkOrder())
                       endOfGame();
                }
                break;

                /* move cursor right */
            case BTN_RIGHT:
                if(x+1 < width)
                {
                    x++;
                    turns++;
                    showTurns();
                    field[oldy][oldx] = field[y][x];
                    field[y][x] = 0;
                    displayField();
                    if(checkOrder())
                       endOfGame();
                }
                break;

                /* move cursor down */
            case BTN_DOWN:
                if(y+1 < height)
                {
                    y++;
                    turns++;
                    showTurns();
                    field[oldy][oldx] = field[y][x];
                    field[y][x] = 0;
                    displayField();
                    if(checkOrder())
                       endOfGame();
                }
                break;

                /* move cursor up */
            case BTN_UP:
                if(y-1 >= 0)
                {
                    y--;
                    turns++;
                    showTurns();
                    field[oldy][oldx] = field[y][x];
                    field[y][x] = 0;
                    displayField();
                    if(checkOrder())
                       endOfGame();
                }
                break;

            case BTN_F2:
                break;

                /* toggle flag under cursor */
            case BTN_F1:

                break;

            case BTN_ON: // new game
                cops->clearScreen(COLOR_GREEN);
                init();
                displayField();
                break;

            case BTN_F3: // settings
                break;
    }
}

