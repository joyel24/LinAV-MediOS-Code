static void draw_piece(int x, int y)
{
	if(worldmap[y][x] ==  0)
		cops->drawBITMAP (&forestB, x*PIECE_DIM, y*PIECE_DIM);
	if(worldmap[y][x] ==  1)
		cops->drawBITMAP (&townB,   x*PIECE_DIM, y*PIECE_DIM);
	if(worldmap[y][x] ==  2)
		cops->drawBITMAP (&waterB,  x*PIECE_DIM, y*PIECE_DIM);
	if(worldmap[y][x] ==  3)
		cops->drawBITMAP (&hillB,   x*PIECE_DIM, y*PIECE_DIM);
}

static void draw_world()
{
   int x = 0;
	int y = 0;

	// ToDo: Scrollbereich berücksichtigen
	for( x=0; x<WORLD_X; x++)
	{
   	for( y=0; y<WORLD_Y; y++)
		{
		   draw_piece(x,y);
		}
	}
}

static void empire_init(void)
{
    cops->clearScreen(COLOR_WHITE);

	 // ToDo: Draw Map out of a file or just generated by prog
    draw_world();

    // Startposition einzeichnen
	 cops->drawRect(COLOR_RED, startposX*PIECE_DIM, startposY*PIECE_DIM, PIECE_DIM, PIECE_DIM);

    cops->putS(COLOR_BLACK, COLOR_WHITE, 230,  5, "Empire V0.001");
    cops->putS(COLOR_BLACK, COLOR_WHITE, 237, 20, "by Schoki");
}

static void move_position(int evt)
{
   char tmp[10];

	if(evt == BTN_RIGHT)
	{
      if(startposX+1 >= WORLD_X) return;
   }
	else if( evt == BTN_LEFT)
	{
      if(startposX-1 < 0)
		{
		   return;
		}
	}
	else if( evt == BTN_DOWN)
	{
      if(startposY+1 >= WORLD_Y) return;
	}
	else if( evt == BTN_UP)
	{
      if(startposY-1 < 0) return;
   }

   // roten Rahmen an der alten Position entfernen
   draw_piece(startposX,startposY);

	// Neue Position markieren
	if(evt == BTN_RIGHT)
      startposX++;
	else if( evt == BTN_LEFT)
      startposX--;
	else if( evt == BTN_DOWN)
      startposY++;
	else if( evt == BTN_UP)
      startposY--;

	cops->fillRect(COLOR_WHITE,228,55,80,25);
	sprintf(tmp,"x:%d y:%d", startposX, startposY);
   cops->putS(COLOR_BLACK, COLOR_WHITE, 230, 60, tmp);

	cops->drawRect(COLOR_RED, startposX*PIECE_DIM, startposY*PIECE_DIM, PIECE_DIM, PIECE_DIM);
}

/* the main game loop */
int eventHandler(int evt)
{
   int i;

	switch (evt) {
			case BTN_OFF:
         case EVT_QUIT:
				/* get out of here */
				stop=1;
				break;

			case BTN_F1:
				break;

			case BTN_F2:
				break;

			case BTN_LEFT:
            move_position(evt);
				break;

			case BTN_RIGHT:
            move_position(evt);
				break;

			case BTN_UP:
            move_position(evt);
				break;

			case BTN_DOWN:
            move_position(evt);
				break;
	}
}

int main(int argc,char * * argv)
{
    int w, h, i;

	 REGISTER(cops,eventHandler,0);
    PACK(cops);

    cops->hideSBar();

    empire_init();

    while (!stop)
 	 {
	 }

    cops->showSBar();

    return;
}

