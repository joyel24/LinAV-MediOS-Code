#include <graphics.h>
#include <osdDSC25.h>
#include <buttons.h>

int main() {
    int cdir=0, c=0;
    int x=160, y=120;
    struct graphicsBuffer screenVideo;
    int px, py;
    int cursorx=160, cursory=120;
    int butslow = 40;

    static int cursor[256] = {
        0x0100, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0100, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0101, 0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0101, 0x0101, 0x0100, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0001, 0x0101, 0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0202, 0x0202, 0x0202, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0001, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0000, 0x0100, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0001, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0100, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0001, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0100, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0001, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0100, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0001, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0100, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0001, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0100, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0001, 0x0000,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101,
        0x0101, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0101};
    
    osdInit();
    
    osdSetComponentConfig(OSD_VIDEO1, 0);
    osdSetComponentConfig(OSD_VIDEO2, 0);
    osdSetComponentConfig(OSD_BITMAP1, 0);
    osdSetComponentConfig(OSD_BITMAP2, 0);
    osdSetComponentConfig(OSD_CURSOR1, 0);
    osdSetComponentConfig(OSD_CURSOR2, 0);
    
    osdSetComponentSize(OSD_VIDEO1, 640, 240);
    osdSetComponentPosition(OSD_VIDEO1, 0x14, 0x13);
    osdSetComponentOffset(OSD_VIDEO1, 0x03c00000);
    osdSetComponentSourceWidth(OSD_VIDEO1, 0x28);
    osdSetComponentConfig(OSD_VIDEO1, OSD_COMPONENT_ENABLE);

    osdSetComponentConfig(OSD_CURSOR2, OSD_COMPONENT_ENABLE);

    screenVideo.offset = 0x03c00000;
    screenVideo.bytesPerLine = 320*4;
    screenVideo.width = 320;
    screenVideo.height = 240;
    screenVideo.bitsPerPixel = 32;

    for (py=0;py<240;py+=8) {
        for (px=0;px<320;px+=8) {
            c = (px-160)*(py-120);
            graphicsBoxf(&screenVideo, px, py, 8, 8, c);
        }
    }
    
    for (c=0;c<16;c++) {
        osdSet16CPallette(0, c, c);
    }
    
    for (c=0;c<256;c++) {
        osdSetCursor2Bitmap(c, cursor[c]);    
    }
    
    while(1) {
        if (butslow==0) {
            butslow=40;
            c = buttonsGetStatus();
            if (c & BUTTONS_AV300_UP) {
                cursory--;
            } else if (c & BUTTONS_AV300_DOWN) {
                cursory++;
            } else if (c & BUTTONS_AV300_LEFT) {
                cursorx--;
            } else if (c & BUTTONS_AV300_RIGHT) {
                cursorx++;
            }
        }
        butslow--;

        if (cursorx<0) cursorx=0;
        if (cursory<0) cursory=0;
        if (cursorx>(319-16)) cursorx=319-16;
        if (cursory>(239-16)) cursory=239-16;

        osdSetComponentPosition(OSD_CURSOR2, (cursorx<<1)+0x18, cursory+0x13);
            
        c = graphicsGetPixel(&screenVideo, x, y);
        if ((c&1)==0) cdir++; else cdir--;
        cdir = cdir & 3;
        c=c + 0x010305;
        graphicsSetPixel(&screenVideo, x, y, c);
        
        if (cdir==0) x++;
        else if (cdir==1) y++;
        else if (cdir==2) x--;
        else if (cdir==3) y--;
        
        if (x>319) x = x - 320;
        if (y>239) y = y - 240;
        if (x<0) x = x + 320;
        if (y<0) y = y + 240;

    }
}
