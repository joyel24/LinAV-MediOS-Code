#include <graphics.h>
#include <osdDSC25.h>
#include <buttons.h>

int main() {
    int cdir=0, c=0;
    int x=160, y=120;
    struct graphicsBuffer screenVideo;
    int px, py;
    int cursorx=160, cursory=120;
    int butslow = 160;
    int butmax = 160;
    
    static int cursor[256] = {
        0x0b00, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0b, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0b00, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0b, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0b00, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0b, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0a, 0x0b00, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0a, 0x0a0b, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0a, 0x0a0a, 0x0b00, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0a, 0x0a0a, 0x0a0b, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0a0a, 0x0a0a, 0x0b0b, 0x0b0b, 0x0b00, 0x0000, 0x0000,
        0x0b0a, 0x0a0b, 0x0b0a, 0x0b00, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0a, 0x0b00, 0x0b0a, 0x0b00, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b0b, 0x0000, 0x000b, 0x0a0b, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0b00, 0x0000, 0x000b, 0x0a0b, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0b0a, 0x0b00, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0b0b, 0x0b00, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000,
        0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000, 0x0000};
    
    osdInitA();
    
    osdSetComponentConfigA(OSD_VIDEO1, 0);
    osdSetComponentConfigA(OSD_VIDEO2, 0);
    osdSetComponentConfigA(OSD_BITMAP1, 0);
    osdSetComponentConfigA(OSD_BITMAP2, 0);
    osdSetComponentConfigA(OSD_CURSOR1, 0);
    osdSetComponentConfigA(OSD_CURSOR2, 0);
    
    osdSetComponentSizeA(OSD_VIDEO1, 640, 240);
    osdSetComponentPositionA(OSD_VIDEO1, 0x14, 0x13);
    osdSetComponentOffsetA(OSD_VIDEO1, 0x03c00000);
    osdSetComponentSourceWidthA(OSD_VIDEO1, 0x28);
    osdSetComponentConfigA(OSD_VIDEO1, OSD_COMPONENT_ENABLE);

    osdSetComponentConfigA(OSD_CURSOR2, OSD_COMPONENT_ENABLE);

    screenVideo.offset = 0x03c00000;
    screenVideo.bytesPerLine = 320*4;
    screenVideo.width = 320;
    screenVideo.height = 240;
    screenVideo.bitsPerPixelShift = 5;
    screenVideo.bitsPerPixel = 32;

    for (py=0;py<240;py+=8) {
        for (px=0;px<320;px+=8) {
            c = (px-160)*(py-120);
            graphicsBoxfA(&screenVideo, px, py, 8, 8, c);
        }
    }
    
    for (c=0;c<16;c++) {
        osdSet16CPalletteA(0, c, c);
    }
    
    for (c=0;c<256;c++) {
        osdSetCursor2BitmapA(c, cursor[c]);    
    }

    while(1) {
    
        if (butslow==0) {
            butslow=butmax;
            c = buttonsGetStatusA();
            if (c & BUTTONS_AV300_UP) {
                cursory--;
                butmax-=2;
            } else if (c & BUTTONS_AV300_DOWN) {
                cursory++;
                butmax-=2;
            } else if (c & BUTTONS_AV300_LEFT) {
                cursorx--;
                butmax-=2;
            } else if (c & BUTTONS_AV300_RIGHT) {
                cursorx++;
                butmax-=2;
            } else {
                butmax = 160;
            }
            if (butmax<30) butmax=30;
        }
        butslow--;

        if (cursorx<0) cursorx=0;
        if (cursory<0) cursory=0;
        if (cursorx>(318)) cursorx=318;
        if (cursory>(238)) cursory=238;

        osdSetComponentPositionA(OSD_CURSOR2, (cursorx<<1)+0x18, cursory+0x13);
            
        c = graphicsGetPixelA(&screenVideo, x, y);
        if ((c&1)==0) cdir++; else cdir--;
        cdir = cdir & 3;
        c=c + 0x010305;
        graphicsSetPixelA(&screenVideo, x, y, c);
        
        if (cdir==0) x++;
        else if (cdir==1) y++;
        else if (cdir==2) x--;
        else if (cdir==3) y--;
        
        if (x>319) x = x - 320;
        if (y>239) y = y - 240;
        if (x<0) x = x + 320;
        if (y<0) y = y + 240;

    }
}
